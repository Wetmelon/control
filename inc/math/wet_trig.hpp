namespace wet::detail {

/**
 * @brief Wrap any angle (radians) into the polynomial's input domain
 *
 * phase_shift offsets the fractional part in half-period units; cos uses
 * 0.5 to fold pi/2 into the same FMA.  No Cody-Waite split -- single-
 * precision cancellation at large |x| is within the ~8 ULP target.
 *
 * @param x            Angle in radians
 * @param phase_shift  Constant offset in half-periods (0 for sin, 0.5 for cos)
 * @return Reduced{frac, period_index}
 */
constexpr Reduced wrap(float x, float phase_shift) {
    float half_periods = x * std::numbers::inv_pi_v<float> + phase_shift;
    float nearest_period = std::nearbyint(half_periods);
    int   period_index = static_cast<int>(nearest_period);
    float frac = half_periods - nearest_period;

    return {frac, period_index};
}

/**
 * @brief Evaluate sin(pi*frac) using the degree-7 minimax polynomial
 *
 * sin(pi*frac) = frac * (c0 + c1*u + c2*u^2 + c3*u^3) with u = frac^2.
 * Precomputing u^2 lets GCC reorganize the FMAs as an even/odd split
 * for pipeline ILP; clang stays in Horner form.
 *
 * @param frac  Fractional half-period in [-0.5, 0.5]
 * @return sin(pi * frac)
 */
constexpr float sin_poly(float frac) {
    float u = frac * frac;
    float u2 = u * u;

    float p = sin_coeffs[0];
    p += sin_coeffs[1] * u;
    p += sin_coeffs[2] * u2;
    p += sin_coeffs[3] * u2 * u;

    return frac * p;
}

/**
 * @brief Horner evaluation of a polynomial in float32
 *
 * Generic polynomial evaluator: p = coeffs[0] + x * (coeffs[1] + x * (...))
 * Compiles to a chain of FMA instructions on FPU architectures.
 *
 * @param x       Evaluation point
 * @param coeffs  Polynomial coefficients, lowest power first
 * @return p(x)
 */
template<size_t N>
constexpr float horner_eval(float x, const float (&coeffs)[N]) {
    static_assert(N >= 1, "horner_eval requires at least one coefficient");
    float p = coeffs[N - 1];
    for (int i = static_cast<int>(N) - 2; i >= 0; --i) {
        p = coeffs[i] + (x * p);
    }
    return p;
}

} // namespace wet::detail
