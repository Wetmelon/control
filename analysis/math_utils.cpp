constexpr float fast_sin_f32(const float x) {
    /* input x is in radians */
    /* Scale the input to [0 1] range from [0 2*PI] , divide input by 2*pi */
    float in = x / (2.0f * M_PI);

    /* Map input value to [0 1] */
    in = in - std::floor(in);

    /* Calculation of index of the table */
    float  findex = (float)FAST_MATH_TABLE_SIZE * in;
    size_t index = (size_t)findex;

    /* when "in" is exactly 1, we need to rotate the index down to 0 */
    if (index >= FAST_MATH_TABLE_SIZE) {
        index = 0;
        findex -= (float)FAST_MATH_TABLE_SIZE;
    }

    /* fractional value calculation */
    const float fract = findex - (float)index;

    /* Read two nearest values of input value from the sin table */
    float a = sinTable_f32[index];
    float b = sinTable_f32[index + 1];

    /* Linear interpolation process */
    float sinVal = (1.0f - fract) * a + fract * b;

    /* Return the output value */
    return (sinVal);
}

constexpr float sin(float x) { return fast_sin_f32(x); }

constexpr float cos(float x) {
    /* input x is in radians */
    /* Scale the input to [0 1] range from [0 2*PI] , divide input by 2*pi, add
     * 0.25 (pi/2) to read sine table */
    return fast_sin_f32(x + (M_PI / 2.0f));
}

